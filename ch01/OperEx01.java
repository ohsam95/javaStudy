package ch01;

public class OperEx01 {

	public static void main(String[] args) {
		int n1, n2;
		n1 = 5;
		n2 = 3;

		// 컨트롤 + s로 컴파일하고 시프트 + f10으로 실행

		System.out.println(n1 + n2);
		System.out.println(n1 - n2);
		System.out.println(n1 / n2);
		System.out.println(n1 * n2);
		System.out.println(n1 % n2); // 나머지
	}
}
